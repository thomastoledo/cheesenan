import { cheeseNaN, cheesePrime, cheesePowerOfN } from 'cheesenan';

console.log('Is NaN:', cheeseNaN(NaN)); // true
console.log('Is Prime:', cheesePrime(7)); // true
console.log('Is Power of 2:', cheesePowerOfN(16, 2)); // true
console.log('Is Power of -3:', cheesePowerOfN(-27, -3)); // true
console.log('Is Perfect Square:', cheesePowerOfN(25, 5)); // true
